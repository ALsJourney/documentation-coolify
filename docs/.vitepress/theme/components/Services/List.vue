const search = ref('')
const selectedCategories = ref(['All'])
const isOpen = ref(false)
const dropdownRef = ref<HTMLElement | null>(null)

const categories = computed(() => {
    const uniqueCategories = new Set(services.map(s => s.category))
    return Array.from(uniqueCategories).sort()
})

// Add click outside handler
const handleClickOutside = (event: MouseEvent) => {
    if (dropdownRef.value && !dropdownRef.value.contains(event.target as Node)) {
        isOpen.value = false
    }
}

// Add and remove event listeners
onMounted(() => {
    document.addEventListener('click', handleClickOutside)
})

onUnmounted(() => {
    document.removeEventListener('click', handleClickOutside)
})

const filteredServices = computed(() =>
    services.filter(s =>
        (selectedCategories.value.includes('All') || selectedCategories.value.includes(s.category)) &&
        s.name.toLowerCase().includes(search.value.toLowerCase())
    )
)

const filteredServicesByCategory = (category: string) => {
    return services.filter(s =>
        s.category === category &&
        s.name.toLowerCase().includes(search.value.toLowerCase())
    )
}

const filteredCategories = computed(() => {
    return categories.value.filter(category =>
        filteredServicesByCategory(category).length > 0
    )
})

const toggleCategory = (category: string) => {
    if (category === 'All') {
        selectedCategories.value = ['All']
        return
    }
    
    const index = selectedCategories.value.indexOf(category)
    if (index === -1) {
        selectedCategories.value = selectedCategories.value.filter(c => c !== 'All')
        selectedCategories.value.push(category)
    } else {
        selectedCategories.value.splice(index, 1)
        if (selectedCategories.value.length === 0) {
            selectedCategories.value = ['All']
        }
    }
}
